package HMS;

/**
 * Enumeration representing the possible statuses of an appointment.
 * - CONFIRMED: The appointment has been confirmed.
 * - CANCELLED: The appointment has been cancelled.
 * - UNKNOWN: The status of the appointment is not yet determined.
 * 
 * @author Omair Nawaz
 * @author Yacine Marabet
 * @author Mir Abbasi
 * @author Abdulrahman Al-Sulaiti
 */
public enum AppointmentStatus {
	CONFIRMED, CANCELLED, UNKNOWN;
}
